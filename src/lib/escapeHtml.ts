/**
 * Escape HTML special characters to prevent XSS in dynamic HTML content.
 * Use this whenever inserting user-provided data into HTML strings.
 */
export function escapeHtml(str: string): string {
  return str
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#039;");
}
